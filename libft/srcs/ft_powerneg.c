#include "libft.h"

double	ft_powerneg(float a, int b)
{
	float pow;

	(a < 0) ? a *= -1 : 0;
	a = 1.0 / a;
	pow = a;
	if (b == 0)
		return (0);
	while (--b != 0)
		a *= pow;
	return (a);
}
